package day10forloopwhileloopnt;

public class WhileLoop02 {

	public static void main(String[] args) {
		/*
		   Find the sum of first 10 counting numbers by using 
		 while loop.
		 */
		
		int i = 1;
		int sum = 0;
		while(i<=10) {
			sum = sum + i;
			i++;
		}
		System.out.println(sum);
		
		

	}

}
